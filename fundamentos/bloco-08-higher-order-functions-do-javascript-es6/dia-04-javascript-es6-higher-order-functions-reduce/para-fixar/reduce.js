// Busca o maior valor em um array.
const numbers = [50, 85, -30, 3, 15];

const getBigger = numbers.reduce((bigger, number) => (bigger > number) ? bigger : number);

console.log(`O maior valor do array é: ${getBigger}`);

// some todos os números pares do array
const numbers2 = [18, 19, 23, 53, 4, 5, 76, 23, 54];

const isEven = numbers2.filter(number => number % 2 === 0);

const sumEven = isEven.reduce((acc, numberEven) => acc + numberEven);

console.log(`A soma dos valores pares é: ${sumEven}`);

